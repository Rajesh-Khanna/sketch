export const getNWords = (n) => {
    var indexs = [];
    while (indexs.length < n) {
        var r = Math.floor(Math.random() * words.length);
        if (indexs.indexOf(r) === -1) indexs.push(r);
    }
    return indexs.map(idx => words[idx])
}